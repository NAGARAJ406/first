package java2424.com.CLT;

public class Switch_case {
	
public static void main(String[] args) {
	int num = 10;
	switch(num) {
	case 10: System.out.println("then num is TEN");
	break;
	
	case 20 :System.out.println("the num id TWENTY");
	break;
	
	case 30 : System.out.println("the given number is THERTY");
	break;
	}
	
	
}
}
